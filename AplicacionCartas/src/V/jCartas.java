    private void btnAgregarCartaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCartaActionPerformed
        String tipo=(String)cboTipo.getSelectedItem();
        if(tipo.equals("Esbirro")){
            Esbirros esbirro = new Esbirros(Integer.parseInt(txtAlter1.getText()), Integer.parseInt(txtAlter2.getText()), txtAlter3.getText(), "1",txtNombre.getText(), txtId.getText(), txtRareza.getText(), Integer.parseInt(txtManaRequerido.getText()), txtDescripcion.getText(), chkDorada.isSelected());
            new C_Esbirro().agregarCarta(esbirro);
        }
        if(tipo.equals("Hechizo")){
            Hechizos hechizo = new Hechizos(txtAlter1.getText(), "2",txtNombre.getText(), txtId.getText(), txtRareza.getText(), Integer.parseInt(txtManaRequerido.getText()), txtDescripcion.getText(),chkDorada.isSelected());
            new C_Hechizo().agregarCarta(hechizo);
        }
        if(tipo.equals("Arma")){
            Arma arma = new Arma(Integer.parseInt(txtAlter1.getText()), Integer.parseInt(txtAlter2.getText()),"3",txtNombre.getText(), txtId.getText(), txtRareza.getText(), Integer.parseInt(txtManaRequerido.getText()), txtDescripcion.getText(),chkDorada.isSelected());
            new C_Arma().agregarCarta(arma);
        }
    }//GEN-LAST:event_btnAgregarCartaActionPerformed

