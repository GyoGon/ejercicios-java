    private void cboTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboTipoActionPerformed
        txtAlter1.setVisible(false);
        txtAlter2.setVisible(false);
        txtAlter3.setVisible(false);
        lblAlter1.setVisible(false);
        lblAlter2.setVisible(false);
        lblAlter3.setVisible(false);
        String tipo=(String)cboTipo.getSelectedItem();
        if(tipo.equals("Esbirro")){
            txtAlter1.setVisible(true);
            txtAlter2.setVisible(true);
            txtAlter3.setVisible(true);
            lblAlter1.setVisible(true);
            lblAlter2.setVisible(true);
            lblAlter3.setVisible(true);
            lblAlter1.setText("Vida");
            lblAlter2.setText("Ataque");
            lblAlter3.setText("Mecanica");
        }
        if(tipo.equals("Hechizo")){
            txtAlter1.setVisible(true);
            lblAlter1.setVisible(true);
            lblAlter1.setText("Desc Habilidad");
        }
        if(tipo.equals("Arma")){
            txtAlter1.setVisible(true);
            txtAlter2.setVisible(true);
            lblAlter1.setVisible(true);
            lblAlter2.setVisible(true);
            lblAlter1.setText("Durabilidad");
            lblAlter2.setText("Ataque Heroe");
        }
    }//GEN-LAST:event_cboTipoActionPerformed

